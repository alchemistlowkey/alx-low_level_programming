#include "main.h"

/**
 * add - check the code
 * @i: first integer
 * @j: second integer
 * Return: k
 */
int add(int i, int j)
{
	int k;

	k = i + j;
	return (k);
}

/**
 * sub - subtraction
 * @a: integer
 * @b: integer
 * Return: c (difference of a and b)
 */
int sub(int a, int b)
{
	int c;

	c = a - b;
	return (c);
}

/**
 * mul - multiply
 * @a: integer
 * @b: integer
 * Return: c (product of a and b)
 */
int mul(int a, int b)
{
	int c;

	c = a * b;
	return (c);
}

/**
 * div - division
 * @a: integer
 * @b: integer
 * Return: c (division of a and b)
 */
int div(int a, int b)
{
	int c;

	c = a / b;
	return (c);
}

/**
 * mod - modulus
 * @a: integer
 * @b: integer
 * Return: c (remainder of a and b)
 */
int mod(int a, int b)
{
	int c;

	c = a % b;
	return (c);
}
